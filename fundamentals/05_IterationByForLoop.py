# Iteration by for loop example in Python

# Iterate over a list
numbers = [1, 2, 3, 4, 5]
for num in numbers:
     print("Number:", num)

# Iterate over a string
text = "Hello"
for char in text:
     print("Character:", char)

# Iterate using range
for i in range(5):
     print("Index:", i)

name = "batman"
for char in name:
     print(char)